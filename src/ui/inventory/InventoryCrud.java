    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Inventory i = new Inventory();
        i.setStoreId(s.getStoreId());
        i.setGoodsId(Integer.parseInt(jLabelId.getText()));
        String x = jTextFieldPrice.getText();
        if(x==null || x==""){
            JOptionPane.showMessageDialog(this,"Price can't be null");
            return;
        }
        try{
            Double.parseDouble(x);
        }
        catch(Exception ex){
            //ex.printStackTrace();
            JOptionPane.showMessageDialog(this,"Wrong Price. Please input a number.");
            return;
        }
        double price = Double.parseDouble(x);
        if(price<0){
            jLabel1.setText("Price can't be negative");
            return;
        }
        i.setSellingPrice(price);
        try {
            iDao.updateInventory(i);
            jLabelId.setText("");
            jLabelName.setText("");
            jTextFieldPrice.setText("");
            showTable();
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

