    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        // TODO add your handling code here:
        int selectedIndex = jTableInventory.getSelectedRow();
        if(selectedIndex < 0){
            JOptionPane.showMessageDialog(this,"Please select a row to delete");
            return;
        }
        DefaultTableModel model = (DefaultTableModel)jTableInventory.getModel();
        int storeId = s.getStoreId();
        int goodsId = Integer.parseInt(model.getValueAt(selectedIndex,0).toString());
        int n = JOptionPane.showConfirmDialog(null, "Confirm to delete ", "",JOptionPane.YES_NO_OPTION);//0/1
        try{
            if(n==0)
                iDao.deleteInventoryByStoreAndGoods(storeId, goodsId);
            showTable();
        }
        catch(Exception e){
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_jButtonDeleteActionPerformed

    private void jButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateActionPerformed
        // TODO add your handling code here:
        int selectedIndex = jTableInventory.getSelectedRow();
        if(selectedIndex < 0){
            JOptionPane.showMessageDialog(this,"Please select a row to update");
            return;
        }
        DefaultTableModel model = (DefaultTableModel)jTableInventory.getModel();
        int storeId = s.getStoreId();
        int goodsId = Integer.parseInt(model.getValueAt(selectedIndex,0).toString());

        try {
            jLabelId.setText(goodsId+"");
            jLabelName.setText(gDao.getGoodsById(goodsId).getGoodsName());
            Inventory i = iDao.getInventoryByStoreIdAndGoodsId(storeId, goodsId);
            jTextFieldPrice.setText(i.getSellingPrice()+"");
            
        } catch (Exception ex) {
            ex.printStackTrace();
        }
        
    }//GEN-LAST:event_jButtonUpdateActionPerformed

